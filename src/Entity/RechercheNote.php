<?php

namespace App\Entity;

use App\Repository\RechercheNoteRepository;
use Doctrine\ORM\Mapping as ORM;


class RechercheNote
{
    private $date;
    private $etat;

    /**
     * @return mixed
     */
    public function getDate()
    {
        return $this->date;
    }

    /**
     * @param mixed $date
     */
    public function setDate($date): void
    {
        $this->date = $date;
    }

    /**
     * @return mixed
     */
    public function getEtat()
    {
        return $this->etat;
    }

    /**
     * @param mixed $etat
     */
    public function setEtat($etat): void
    {
        $this->etat = $etat;
    }


}
